public class ConditionalOperator {
    public static void main(String args[]){
        int a, b;
        a=30;
        b=20;
        switch (a){
            case 10: {
                System.out.println("A is 10");
            }
                break;
            case 20:{
                System.out.println("A is 20");
            }
            break;
            case 30:{
                System.out.println("A is 30");
            }
            break;

        }

    }
}
